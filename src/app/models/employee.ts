export class Employee {
  file: number;
  first: string;
  last: string;
  sex: string;
  salary: number;

	constructor(file: number, first: string, last: string, sex: string, salary: number) {
		this.file = file;
		this.first = first;
		this.last = last;
		this.sex = sex;
		this.salary = salary;
	}

}
